    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtName.getText().length() > 0) {
            try {
                Statement state = connection.createStatement();
                database.insert("publish", "title, city, address, phone", "'" + txtName.getText() + "','"
                        + txtCity.getText() + "','" + txtAddress.getText() + "','" + txtPhone.getText() + "'", state);
                state.close();
            } catch (Exception e) {
                System.out.println("Publish:insert:error:" + e.getLocalizedMessage());
            }
            clear();
            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


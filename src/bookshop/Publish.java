    private void populateTable() {
        try {
            Statement state = connection.createStatement();
            table.setModel(DbUtils.resultSetToTableModel(database.select("publish", "*", null, "id", state)));
            state.close();
        } catch (Exception e) {
            System.out.println("Publish:populateTable:error:" + e.getLocalizedMessage());
        }
    }


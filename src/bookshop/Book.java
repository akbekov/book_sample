    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtName.getText().length() > 0) {
            try {
                Statement state = connection.createStatement();
                database.insert("book", "title, id_author,id_publish,price,amount,date_create",
                        "'" + txtName.getText() + "',"
                        + database.getIdByField("author", "name = '" + comboAuthor.getSelectedItem().toString() + "'", state) + ","
                        + database.getIdByField("publish", "title = '" + comboPublish.getSelectedItem().toString() + "'", state) + ","
                        + "" + txtPrice.getText() + ","
                        + "" + txtAmount.getText() + ","
                        + "'" + txtDate.getDate() + "'", state);
                state.close();
            } catch (Exception e) {
                System.out.println("Book:insert:error:" + e.getLocalizedMessage());
            }
            clear();
            populateTable();
        }
    }//GEN-LAST:event_jButton1ActionPerformed


    private void populateCombo() {
        try {
            comboAuthor.removeAllItems();
            comboPublish.removeAllItems();
            Statement state = connection.createStatement();
            ResultSet rs = database.select("author", "name", null, "name", state);
            while (rs.next()) {
                comboAuthor.addItem(rs.getString("name"));
            }
            rs.close();
            rs = database.select("publish", "title", null, "title", state);
            while (rs.next()) {
                comboPublish.addItem(rs.getString("title"));
            }
            state.close();
        } catch (Exception e) {
            System.out.println("Book:populateCombo:error:" + e.getLocalizedMessage());
        }
    }

    private void populateTable() {
        try {
            Statement state = connection.createStatement();
            table.setModel(DbUtils.resultSetToTableModel(database.select("book b, author a, publish p", "b.id, b.title, a.name, p.title, b.price, b.amount, b.date_create",
                    "b.id_author = a.id and b.id_publish = p.id", "b.id", state)));
            state.close();
        } catch (Exception e) {
            System.out.println("Book:populateTable:error:" + e.getLocalizedMessage());
        }
    }


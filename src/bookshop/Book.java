    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.getSelectedRow();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        if (row != -1) {
            try {
                txtId.setText(table.getValueAt(row, 0).toString());
                txtName.setText(table.getValueAt(row, 1).toString());
                comboAuthor.setSelectedItem(table.getValueAt(row, 2).toString());
                comboPublish.setSelectedItem(table.getValueAt(row, 3).toString());
                txtPrice.setText(table.getValueAt(row, 4).toString());
                txtAmount.setText(table.getValueAt(row, 5).toString());
                txtDate.setDate(sdf.parse(table.getValueAt(row, 6).toString()));
            } catch (ParseException e) {
                System.out.println("Book:click:error:" + e.getLocalizedMessage());
            }
        }
    }//GEN-LAST:event_tableMouseClicked

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String text = txtSearch.getText();
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(table.getModel());
        if (text.length() == 0) {
            rowSorter.setRowFilter(null);
        } else {
            rowSorter.setRowFilter(RowFilter.regexFilter(text));
        }
        table.setRowSorter(rowSorter);
    }//GEN-LAST:event_txtSearchKeyReleased

